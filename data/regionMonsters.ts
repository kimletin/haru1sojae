export interface RegionMonster {
  name: string;
  level: number;
  hp: number;
  exp: number;
  maps: string[]; // 출현 위치
}

export interface MonsterRegion {
  region: string;
  monsters: RegionMonster[];
}

const normalizeMap = (s: string) => s.replace(/\s+/g, '');

// 정규화된 맵 이름 → 해당 맵에 젠되는 몬스터 목록 (경계 맵은 2종 이상)
let _mapLookup: Map<string, RegionMonster[]> | null = null;
function mapLookup(): Map<string, RegionMonster[]> {
  if (_mapLookup) return _mapLookup;
  const lk = new Map<string, RegionMonster[]>();
  for (const r of REGION_MONSTERS) {
    for (const mon of r.monsters) {
      for (const mp of mon.maps) {
        const key = normalizeMap(mp);
        (lk.get(key) ?? lk.set(key, []).get(key)!).push(mon);
      }
    }
  }
  _mapLookup = lk;
  return lk;
}

/** 사냥터(맵) 이름으로 해당 위치에 젠되는 몬스터를 찾는다. 공백 차이는 무시하고, 정확 매칭 실패 시 레벨로 폴백한다. */
export function getMonstersAtMap(mapName: string, fallbackLevels: number[] = []): RegionMonster[] {
  const found = mapLookup().get(normalizeMap(mapName));
  if (found && found.length) return found;
  if (fallbackLevels.length) {
    const lv = new Set(fallbackLevels);
    return REGION_MONSTERS.flatMap(r => r.monsters).filter(m => lv.has(m.level));
  }
  return [];
}

